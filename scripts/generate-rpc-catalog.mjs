#!/usr/bin/env node

/**
 * generate-rpc-catalog.mjs
 *
 * Auto-generates docs/rpc-catalog.md from:
 * 1. src/data/rpc-contracts.ts (Zod schema names)
 * 2. SQL migrations (function signatures)
 * 3. src/data/*.ts (which DAL file calls which RPC)
 *
 * Run: npm run docs:rpcs
 */

import { readFileSync, writeFileSync, readdirSync } from 'fs';
import { join } from 'path';

const ROOT = process.cwd();

// 1. Extract RPC names from RPC_REGISTRY in rpc-contracts.ts
function extractRpcRegistry() {
    const content = readFileSync(join(ROOT, 'src/data/rpc-contracts.ts'), 'utf8');
    const registryMatch = content.match(/RPC_REGISTRY\s*=\s*\{([^}]+)\}/s);
    if (!registryMatch) return [];

    const entries = registryMatch[1].matchAll(/['"]?(\w+)['"]?\s*:/g);
    return [...entries].map(m => m[1]);
}

// 2. Extract Zod schema fields for each schema
function extractSchemaFields(schemaName) {
    const content = readFileSync(join(ROOT, 'src/data/rpc-contracts.ts'), 'utf8');
    const pattern = new RegExp(`${schemaName}\\s*=\\s*z\\.(?:object|array)\\(([\\s\\S]*?)\\);`, 'm');
    const match = content.match(pattern);
    if (!match) return [];

    const fields = [...match[1].matchAll(/(\w+)\s*:/g)].map(m => m[1]);
    return fields;
}

// 3. Find which DAL file calls each RPC
function findDalFile(rpcName) {
    const dataDir = join(ROOT, 'src/data');
    const files = readdirSync(dataDir).filter(f => f.endsWith('.ts') && !f.includes('test') && !f.includes('__'));

    for (const file of files) {
        const content = readFileSync(join(dataDir, file), 'utf8');
        if (content.includes(`'${rpcName}'`) || content.includes(`"${rpcName}"`)) {
            return file;
        }
    }
    return 'unknown';
}

// 4. Find the latest SQL definition for each RPC
function findSqlSignature(rpcName) {
    const migDir = join(ROOT, 'supabase/migrations');
    const files = readdirSync(migDir).filter(f => f.endsWith('.sql')).sort().reverse();

    for (const file of files) {
        const content = readFileSync(join(migDir, file), 'utf8');
        const pattern = new RegExp(`CREATE OR REPLACE FUNCTION[\\s\\S]*?${rpcName}\\(([^)]+)\\)`, 'i');
        const match = content.match(pattern);
        if (match) {
            const params = match[1]
                .split(',')
                .map(p => p.trim().replace(/\s+/g, ' '))
                .filter(p => p.length > 0);
            return { params, file };
        }
    }
    return { params: [], file: 'not found' };
}

// Generate
const rpcs = extractRpcRegistry();
const lines = [
    '# RPC Catalog',
    '',
    '> Auto-generated by `scripts/generate-rpc-catalog.mjs`',
    `> Last updated: ${new Date().toISOString().split('T')[0]}`,
    '',
    `Found **${rpcs.length}** RPCs in \`RPC_REGISTRY\`.`,
    '',
    '| RPC | DAL File | Zod Schema Fields | SQL Params | Defined In |',
    '|-----|----------|------------------|------------|------------|',
];

for (const rpc of rpcs) {
    const dalFile = findDalFile(rpc);
    const { params, file: sqlFile } = findSqlSignature(rpc);
    const schemaName = rpc.replace(/_([a-z])/g, (_, c) => c.toUpperCase()).replace(/^(.)/, c => c.toUpperCase()) + 'Schema';

    // Try exact match first, then fuzzy
    let fields = extractSchemaFields(schemaName);
    if (fields.length === 0) {
        // Try common patterns
        const content = readFileSync(join(ROOT, 'src/data/rpc-contracts.ts'), 'utf8');
        const rpcLine = content.split('\n').find(l => l.includes(`'${rpc}'`) || l.includes(`"${rpc}"`));
        if (rpcLine) {
            const refMatch = rpcLine.match(/:\s*(\w+Schema)/);
            if (refMatch) fields = extractSchemaFields(refMatch[1]);
        }
    }

    const fieldStr = fields.length > 0 ? fields.slice(0, 5).join(', ') + (fields.length > 5 ? '...' : '') : '—';
    const paramStr = params.length > 0 ? params.map(p => p.split(' ')[0]).join(', ') : '—';
    const sqlShort = sqlFile === 'not found' ? '—' : sqlFile.replace(/\d{14}_/, '').replace('.sql', '');

    lines.push(`| \`${rpc}\` | ${dalFile} | ${fieldStr} | ${paramStr} | ${sqlShort} |`);
}

lines.push('');
lines.push('---');
lines.push('');
lines.push('## How to Update');
lines.push('');
lines.push('```bash');
lines.push('npm run docs:rpcs');
lines.push('```');
lines.push('');
lines.push('This reads `rpc-contracts.ts` and the latest migrations to regenerate this file.');

const output = lines.join('\n');
const outPath = join(ROOT, 'docs/rpc-catalog.md');
writeFileSync(outPath, output);
console.log(`✅ Generated ${outPath} (${rpcs.length} RPCs)`);
